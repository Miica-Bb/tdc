    private void cargarTabla() {
        DefaultTableModel modeladoTabla = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        
        String titulos[] = {"id", "modelo", "marca", "motor", "color", "patente", "cantPuertas"};
        modeladoTabla.setColumnIdentifiers(titulos);
        
        ArrayList<Automovil> listaAutos = controlL.buscarListaAutos();
        if(listaAutos != null) {
            for (Automovil auto : listaAutos) {
                Object[] objeto = {auto.getId(), auto.getModelo(), auto.getMarca(), auto.getMotor(), auto.getColor(), auto.getPatente(), auto.getCantidadPuertas()};
                modeladoTabla.addRow(objeto);
            }
        }
        
        tablaAutos.setModel(modeladoTabla);
    }

    private void mostrarMensaje(String mensaje, String tipo, String titulo) {
        JOptionPane optionPane = new JOptionPane(mensaje);
        
        if(tipo.equals("info")) {
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        }else if (tipo.equals("error")) {
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }
