    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if(tablaAutos.getRowCount() > 0) {
            if(tablaAutos.getSelectedRow() != -1) {
                int id = Integer.parseInt(String.valueOf(tablaAutos.getValueAt(tablaAutos.getSelectedRow(), 0)));
                
                controlL.eliminar(id);
                
                mostrarMensaje("Se eliminó correctamente", "info", "Eliminado exitoso");
                
                cargarTabla();
            } else {
                mostrarMensaje("No se seleccionó ninguna fila", "error", "Error al eliminar");
            }
        } else {
            mostrarMensaje("No hay nada para eliminar", "error", "Error al eliminar");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if(tablaAutos.getRowCount() > 0) {
            if(tablaAutos.getSelectedRow() != -1) {
                int id = Integer.parseInt(String.valueOf(tablaAutos.getValueAt(tablaAutos.getSelectedRow(), 0)));
                
                EditarAuto pantallaEdicion = new EditarAuto(id);
                pantallaEdicion.setLocationRelativeTo(null);
                pantallaEdicion.setVisible(true);
                
                this.dispose();
            } else {
                mostrarMensaje("No se seleccionó ninguna fila", "error", "Error al editar");
            }
        } else {
            mostrarMensaje("No hay nada para editar", "error", "Error al editar");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    

