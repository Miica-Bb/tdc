    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (tablaMascotas.getRowCount() > 0) {
            if (tablaMascotas.getSelectedRow() != -1) {
                int num_cliente = Integer.parseInt(String.valueOf(tablaMascotas.getValueAt(tablaMascotas.getSelectedRow(), 0)));

                controlL.eliminarMascota(num_cliente);

                mostrarMensaje("Mascota eliminada correctamente", "info", "Eliminado exitoso");
                
                cargarTabla();
                
            } else {
                mostrarMensaje("No seleccionó ninguna mascota", "error", "Error al eliminar");
            }
        } else {
            mostrarMensaje("No hay nada para eliminar", "error", "Error al eliminar");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (tablaMascotas.getRowCount() > 0) {
            if (tablaMascotas.getSelectedRow() != -1) {
                int num_cliente = Integer.parseInt(String.valueOf(tablaMascotas.getValueAt(tablaMascotas.getSelectedRow(), 0)));

                EditarDatos pantalla = new EditarDatos(num_cliente);
                pantalla.setLocationRelativeTo(null);
                pantalla.setVisible(true);
                
                this.dispose();
                
            } else {
                mostrarMensaje("No seleccionó ninguna mascota", "error", "Error al editar");
            }
        } else {
            mostrarMensaje("No hay nada para editar", "error", "Error al editar");
        }
        
        
    }//GEN-LAST:event_btnEditarActionPerformed

