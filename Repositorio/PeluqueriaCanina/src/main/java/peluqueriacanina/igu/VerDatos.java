    public void mostrarMensaje(String mensaje, String tipo, String titulo) {
        JOptionPane optionPane = new JOptionPane(mensaje);
        
        if (tipo.equals("info")) {
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        } else if (tipo.equals("error")) {
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }

    private void cargarTabla() {
        DefaultTableModel modeloTabla = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        String titulos[] = {"num_cliente", "Nombre", "Raza", "Color", "Alérgico", "At. Especial", "Observaciones", "Dueño", "Celular", "Dirección"};

        modeloTabla.setColumnIdentifiers(titulos);

        ArrayList<Mascota> listaMascotas = controlL.buscarListaMascotas();

        if (listaMascotas != null) {
            for (Mascota masco : listaMascotas) {
                Object[] objeto = {masco.getNum_cliente(), masco.getNombre_perro(), masco.getRaza(), masco.getColor(), masco.getEsAlergico(),
                    masco.getAtencion_especial(), masco.getObservaciones(),
                    masco.getDuenio().getNombre_duenio(), masco.getDuenio().getCelular(), masco.getDuenio().getDireccion()};

                modeloTabla.addRow(objeto);
            }
        }

        tablaMascotas.setModel(modeloTabla);
    }
