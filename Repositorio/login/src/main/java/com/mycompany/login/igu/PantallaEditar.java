    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        usr = controlL.buscarUsuario(id);

        txtNombre.setText(usr.getNombreUsuario());
        txtContrasenia.setText(usr.getContrasenia());

        List<Rol> listaRoles = controlL.buscarListaRoles();

        if (listaRoles != null) {
            for (Rol rol : listaRoles) {
                cmbRol.addItem(rol.getNombreRol());
            }
        }

        String rol = usr.getUnRol().getNombreRol();

        int cantItems = cmbRol.getItemCount();
        for (int i = 0; i < cantItems; i++) {
            if (String.valueOf(cmbRol.getItemAt(i)).equals(rol)) {
                cmbRol.setSelectedIndex(i);
                break;
            }
        }
    }//GEN-LAST:event_formWindowOpened

