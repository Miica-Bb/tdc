    private void cargarTabla() {
        DefaultTableModel modeloTabla = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        
        String titulos[] = {"id", "nombre_usuario", "contraseña", "rol", "descripción_rol"};
        modeloTabla.setColumnIdentifiers(titulos);
        
        List<Usuario> listaUsuarios = controlL.buscarListaUsuarios();
        if(listaUsuarios != null) {
            for (Usuario unUser : listaUsuarios) {
                Object[] atributo = {unUser.getId(), unUser.getNombreUsuario(), unUser.getContrasenia(), 
                    unUser.getUnRol().getNombreRol(), unUser.getUnRol().getDescripcion()};
                
                modeloTabla.addRow(atributo);
            }
        }
        
        tablaUsuarios.setModel(modeloTabla);
    }
    
    private void mostrarMensaje(String mensaje, String tipo, String titulo) {
        JOptionPane optionPane = new JOptionPane(mensaje);
        
        if(tipo.equals("info")) {
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        } else if (tipo.equals("error")) {
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        }
        
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }
