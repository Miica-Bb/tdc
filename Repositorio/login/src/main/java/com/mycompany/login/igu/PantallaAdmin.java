    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if(tablaUsuarios.getRowCount() > 0) {
            if(tablaUsuarios.getSelectedRow() != -1) {
                int id = Integer.parseInt(String.valueOf(tablaUsuarios.getValueAt(tablaUsuarios.getSelectedRow(), 0)));
                
                controlL.eliminarUsuario(id);
                mostrarMensaje("Usuario eliminado correctamente", "info", "Borrado Exitoso");
                
                cargarTabla();
            } else {
                mostrarMensaje("No se seleccionó ninguna fila", "error", "Error al borrar");
            }
        } else {
            mostrarMensaje("La tabla está vacía, no hay nada que borrar", "error", "Error al borrar");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if(tablaUsuarios.getRowCount() > 0) {
            if(tablaUsuarios.getSelectedRow() != -1) {
                int id = Integer.parseInt(String.valueOf(tablaUsuarios.getValueAt(tablaUsuarios.getSelectedRow(), 0)));
                
                PantallaEditar pEditar = new PantallaEditar(controlL, id);
                pEditar.setLocationRelativeTo(null);
                pEditar.setVisible(true);
                
                cargarTabla();
            } else {
                mostrarMensaje("No se seleccionó ninguna fila", "error", "Error al editar");
            }
        } else {
            mostrarMensaje("La tabla está vacía, no hay nada que editar", "error", "Error al editar");   
        }
    }//GEN-LAST:event_btnEditarActionPerformed



